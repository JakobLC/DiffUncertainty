"""Create patient-balanced train/val/test splits for the NPC dataset.

This script consumes the metadata generated by ``process_npc.py`` and creates
deterministic splits based on 3D patient volumes (not individual 2D slices).

Processing summary:

* All patient volumes are pooled together (both from training and validation splits).
* Patients are sorted deterministically and then divided into:
  - 64% for training
  - 16% for validation
  - 20% for testing
* All 2D slices from a patient are kept together in the same split.
* The resulting ``splits.pkl`` references the processed dataset via relative paths
  of the form ``images/<image_file>`` and includes:
  - Standard splits: train, val, id_test
  - OOD augmented splits (same paths as id_test): ood_noise, ood_hist, ood_gibbs
  - Empty unlabeled pools: id_unlabeled_pool, ood_unlabeled_pool

Output path: ``values_datasets/npc<image_size>/splits/ood_aug/firstCycle/splits.pkl``

The OOD splits are populated by ``create_npc_augmented_ood.py``, which generates
augmented versions in the respective augmented folders.

Usage:
  python create_splits.py  # Creates npc128 splits (default)
  python create_splits.py --image-size 64  # Creates npc64 splits
"""

from __future__ import annotations

import argparse
import csv
import logging
from pathlib import Path
from typing import Dict, List, Set
import pickle

import numpy as np

DATASETS_ROOT = Path("/home/jloch/Desktop/diff/luzern/values_datasets")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Create patient-based train/val/test splits for NPC",
    )
    parser.add_argument(
        "--image-size",
        type=int,
        default=128,
        help="Image size variant to create splits for (e.g., 64 or 128)",
    )
    parser.add_argument(
        "--train-ratio",
        type=float,
        default=0.64,
        help="Fraction of patients for training",
    )
    parser.add_argument(
        "--val-ratio",
        type=float,
        default=0.16,
        help="Fraction of patients for validation",
    )
    parser.add_argument(
        "--test-ratio",
        type=float,
        default=0.20,
        help="Fraction of patients for testing",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=123,
        help="Random seed for shuffling patients",
    )
    parser.add_argument(
        "--verbose",
        action="store_true",
        help="Increase logging verbosity",
    )
    return parser.parse_args()


def setup_logging(verbose: bool) -> None:
    logging.basicConfig(
        level=logging.DEBUG if verbose else logging.INFO,
        format="[%(asctime)s] %(levelname)s:%(name)s:%(message)s",
    )


def load_metadata(metadata_path: Path) -> List[Dict[str, str]]:
    """Load metadata CSV file."""
    if not metadata_path.is_file():
        raise FileNotFoundError(f"Metadata file not found: {metadata_path}")
    
    with metadata_path.open() as handle:
        reader = csv.DictReader(handle)
        required = {"sample_id", "image_file"}
        if reader.fieldnames is None or not required.issubset(reader.fieldnames):
            missing = required - set(reader.fieldnames or [])
            raise ValueError(
                f"Metadata file missing required columns: {sorted(missing)}",
            )
        rows = [row for row in reader]
    
    if not rows:
        raise ValueError("Metadata file is empty; cannot generate splits")
    
    return rows


def extract_patient_id(sample_id: str) -> str:
    """Extract patient ID from sample_id.
    
    Expected format: "train0_slice000" or "val23_slice001"
    Returns the numeric part: "0", "23", etc.
    """
    # sample_id format: "{split_name}{patient_id}_slice{slice_idx}"
    # e.g., "train0_slice000" or "val23_slice001"
    parts = sample_id.split("_")
    if len(parts) < 2:
        raise ValueError(f"Cannot parse sample_id: {sample_id}")
    
    # First part is like "train0" or "val23"
    prefix_part = parts[0]
    # Remove the leading "train" or "val"
    if prefix_part.startswith("train"):
        patient_id = prefix_part[5:]  # Remove "train"
    elif prefix_part.startswith("val"):
        patient_id = prefix_part[3:]  # Remove "val"
    else:
        raise ValueError(f"Cannot parse sample_id: {sample_id}")
    
    return patient_id


def group_by_patient(rows: List[Dict[str, str]]) -> Dict[str, List[str]]:
    """Group image paths by patient ID."""
    patient_to_images: Dict[str, List[str]] = {}
    
    for row in rows:
        sample_id = row["sample_id"]
        image_file = row["image_file"]
        image_path = f"images/{image_file}"
        
        patient_id = extract_patient_id(sample_id)
        
        if patient_id not in patient_to_images:
            patient_to_images[patient_id] = []
        patient_to_images[patient_id].append(image_path)
    
    return patient_to_images


def create_splits(
    patient_to_images: Dict[str, List[str]],
    train_ratio: float,
    val_ratio: float,
    test_ratio: float,
    seed: int,
) -> tuple:
    """Split patients into train/val/test sets.
    
    Returns:
        (train_images, val_images, test_images)
    """
    # Sort patients deterministically
    patient_ids = sorted(patient_to_images.keys())
    n_patients = len(patient_ids)
    
    # Verify ratios sum to ~1.0
    total_ratio = train_ratio + val_ratio + test_ratio
    if not (0.99 <= total_ratio <= 1.01):
        logging.warning(
            "Split ratios sum to %.3f (expected 1.0); normalizing",
            total_ratio,
        )
        train_ratio /= total_ratio
        val_ratio /= total_ratio
        test_ratio /= total_ratio
    
    # Shuffle patients with seed
    rng = np.random.default_rng(seed)
    shuffled_indices = rng.permutation(n_patients)
    shuffled_patients = [patient_ids[i] for i in shuffled_indices]
    
    # Split indices
    n_train = int(np.round(n_patients * train_ratio))
    n_val = int(np.round(n_patients * val_ratio))
    # Remaining go to test
    n_test = n_patients - n_train - n_val
    
    train_patients = shuffled_patients[:n_train]
    val_patients = shuffled_patients[n_train : n_train + n_val]
    test_patients = shuffled_patients[n_train + n_val :]
    
    logging.info(
        "Split: %d train / %d val / %d test patients",
        len(train_patients),
        len(val_patients),
        len(test_patients),
    )
    
    # Collect all images for each split
    train_images = []
    val_images = []
    test_images = []
    
    for patient_id in train_patients:
        train_images.extend(patient_to_images[patient_id])
    
    for patient_id in val_patients:
        val_images.extend(patient_to_images[patient_id])
    
    for patient_id in test_patients:
        test_images.extend(patient_to_images[patient_id])
    
    # Sort for consistency
    train_images = sorted(train_images)
    val_images = sorted(val_images)
    test_images = sorted(test_images)
    
    logging.info(
        "Total 2D slices: %d train / %d val / %d test",
        len(train_images),
        len(val_images),
        len(test_images),
    )
    
    return train_images, val_images, test_images


def ensure_output_path(output_path: Path) -> Path:
    """Ensure output directory exists."""
    if output_path.suffix != ".pkl":
        output_path = output_path / "splits.pkl"
    output_path.parent.mkdir(parents=True, exist_ok=True)
    return output_path


def main() -> None:
    args = parse_args()
    setup_logging(args.verbose)
    
    # Derive dataset name from image size
    dataset_name = f"npc{args.image_size}"
    
    # Setup paths
    dataset_root = (DATASETS_ROOT / dataset_name).expanduser().resolve()
    processed_root = dataset_root / "preprocessed"
    
    if not processed_root.is_dir():
        raise FileNotFoundError(
            f"Processed dataset directory not found: {processed_root}. "
            f"Did you run process_npc.py?",
        )
    
    metadata_path = processed_root / "metadata.csv"
    output_path = dataset_root / "splits" / "ood_aug" / "firstCycle" / "splits.pkl"
    output_path = ensure_output_path(output_path)
    
    # Load and process metadata
    logging.info("Loading metadata from %s", metadata_path)
    rows = load_metadata(metadata_path)
    logging.info("Loaded %d image entries from metadata", len(rows))
    
    # Group by patient
    patient_to_images = group_by_patient(rows)
    logging.info("Found %d unique patients", len(patient_to_images))
    
    # Create splits
    train_images, val_images, test_images = create_splits(
        patient_to_images,
        train_ratio=args.train_ratio,
        val_ratio=args.val_ratio,
        test_ratio=args.test_ratio,
        seed=args.seed,
    )
    
    # Create split structure (single fold, not cross-validation)
    empty = np.array([], dtype=object)
    test_images_array = np.array(test_images, dtype=object)
    splits = [
        {
            "train": np.array(train_images, dtype=object),
            "val": np.array(val_images, dtype=object),
            "id_test": test_images_array,
            "ood_noise": test_images_array,
            "ood_hist": test_images_array,
            "ood_gibbs": test_images_array,
            "id_unlabeled_pool": empty,
            "ood_unlabeled_pool": empty,
        }
    ]
    
    # Save splits
    with output_path.open("wb") as handle:
        pickle.dump(splits, handle)
    
    logging.info("Saved splits to %s", output_path)


if __name__ == "__main__":
    main()
